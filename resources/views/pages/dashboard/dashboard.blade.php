                        <h2 class="text-xl font-bold  text-sm">Purpose</h2>
                        <p class="text-gray-600 mt-2">The purpose of this District Multi-Sectoral Nutrition Program Design Decision Tool is to assist multi-sectoral district-level stakeholders in health (including nutrition, family planning), education, social protection, agriculture, and water, sanitation, and hygiene in designing activities to address undernutrition in their district. </p>
                    </div>
                    <div class="mt-8">
                        <h2 class="text-xl font-bold  text-sm">Approach</h2>
                        <p class="text-gray-600 mt-2">Through facilitated discussions, stakeholders will review existing data; prioritize behaviors; discuss platforms, channels, and services; examine barriers; and design activities that meet the needs of their district. The tool provides a logical step-by-step process and is divided into 7 steps. The tool encourages stakeholders to use data in their decision-making process. The district profile sheet accompanies this tool. Since district-level data are not always available, we recommend stakeholders use their best judgment. Given USAID Integrated Nutrition's emphasis on addressing the needs of underserved populations, each step of the tool guides stakeholders to consider data and activities for everyone in their district and for underserved populations.</p>
